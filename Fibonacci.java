class Fibonacci
{  
    public static void main(String args[])  
     {    
 	int no1=0,no2=1,no3,i,n=10;    
 	System.out.print(n1+" "+n2);
 	for(i=2;i<n;++i) 
	 {    
  	    no3=no1+no2;    
	    System.out.print(" "+no3);    
  	    no1=no2;    
   	    no2=no3;    
 	}    
  
      }
}  